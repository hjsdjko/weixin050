		watch:{
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下拉框
			res = await this.$api.option(`shuiguofenlei`,`shuiguofenlei`,{});
			this.shuiguofenleiOptions = res.data;
            this.shuiguofenleiOptions.unshift("请选择水果分类");

			// 如果有登录，获取登录后保存的userid
